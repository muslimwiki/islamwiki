/**
 * Arabic Language Plugin
 * Handles translation of interface text to Arabic
 */
class ArabicLanguagePlugin {
    constructor() {
        this.translations = ARABIC_TRANSLATIONS;
        this.isActive = false;
    }

    /**
     * Activate Arabic translations
     */
    activate() {
        this.isActive = true;
        this.translatePage();
        this.setupTranslationObserver();
    }

    /**
     * Deactivate Arabic translations
     */
    deactivate() {
        this.isActive = false;
        this.restoreOriginalText();
    }

    /**
     * Translate the entire page
     */
    translatePage() {
        if (!this.isActive) return;

        // Translate navigation items
        this.translateNavigation();
        
        // Translate common elements
        this.translateCommonElements();
        
        // Translate dynamic content
        this.translateDynamicContent();
        
        // Add RTL support
        this.applyRTLSupport();
    }

    /**
     * Translate navigation elements
     */
    translateNavigation() {
        // Main navigation links
        const navLinks = document.querySelectorAll('.bismillah-nav-link');
        navLinks.forEach(link => {
            const text = link.textContent.trim();
            if (this.translations[text]) {
                this.translateElement(link, text);
            }
        });

        // Dropdown items
        const dropdownItems = document.querySelectorAll('.dropdown-item');
        dropdownItems.forEach(item => {
            const text = item.textContent.trim();
            if (this.translations[text]) {
                this.translateElement(item, text);
            }
        });

        // Header actions
        const headerActions = document.querySelectorAll('.bismillah-header-actions a, .bismillah-header-actions button');
        headerActions.forEach(action => {
            const text = action.textContent.trim();
            if (this.translations[text]) {
                this.translateElement(action, text);
            }
        });
    }

    /**
     * Translate common page elements
     */
    translateCommonElements() {
        // Page titles
        const titles = document.querySelectorAll('h1, h2, h3, h4, h5, h6');
        titles.forEach(title => {
            const text = title.textContent.trim();
            if (this.translations[text]) {
                this.translateElement(title, text);
            }
        });

        // Buttons
        const buttons = document.querySelectorAll('button, .btn, input[type="submit"], input[type="button"]');
        buttons.forEach(button => {
            const text = button.textContent.trim();
            if (this.translations[text]) {
                this.translateElement(button, text);
            }
        });

        // Links
        const links = document.querySelectorAll('a');
        links.forEach(link => {
            const text = link.textContent.trim();
            if (this.translations[text]) {
                this.translateElement(link, text);
            }
        });

        // Labels
        const labels = document.querySelectorAll('label');
        labels.forEach(label => {
            const text = label.textContent.trim();
            if (this.translations[text]) {
                this.translateElement(label, text);
            }
        });

        // Placeholders
        const inputs = document.querySelectorAll('input[placeholder], textarea[placeholder]');
        inputs.forEach(input => {
            const placeholder = input.getAttribute('placeholder');
            if (placeholder && this.translations[placeholder]) {
                input.setAttribute('placeholder', this.translations[placeholder]);
            }
        });
    }

    /**
     * Translate dynamic content
     */
    translateDynamicContent() {
        // This will handle content that gets added dynamically
        // We'll use a MutationObserver to catch new content
    }

    /**
     * Setup observer for dynamic content
     */
    setupTranslationObserver() {
        const observer = new MutationObserver((mutations) => {
            mutations.forEach((mutation) => {
                if (mutation.type === 'childList') {
                    mutation.addedNodes.forEach((node) => {
                        if (node.nodeType === Node.ELEMENT_NODE) {
                            this.translateElement(node);
                        }
                    });
                }
            });
        });

        observer.observe(document.body, {
            childList: true,
            subtree: true
        });
    }

    /**
     * Translate a specific element
     */
    translateElement(element, originalText = null) {
        if (!this.isActive) return;

        if (!originalText) {
            originalText = element.textContent.trim();
        }

        if (this.translations[originalText]) {
            // Store original text if not already stored
            if (!element.hasAttribute('data-original-text')) {
                element.setAttribute('data-original-text', originalText);
            }
            
            // Apply translation
            element.textContent = this.translations[originalText];
        }

        // Recursively translate child elements
        const children = element.querySelectorAll('*');
        children.forEach(child => {
            const childText = child.textContent.trim();
            if (this.translations[childText]) {
                if (!child.hasAttribute('data-original-text')) {
                    child.setAttribute('data-original-text', childText);
                }
                child.textContent = this.translations[childText];
            }
        });
    }

    /**
     * Restore original text
     */
    restoreOriginalText() {
        const translatedElements = document.querySelectorAll('[data-original-text]');
        translatedElements.forEach(element => {
            const originalText = element.getAttribute('data-original-text');
            if (originalText) {
                element.textContent = originalText;
                element.removeAttribute('data-original-text');
            }
        });
    }

    /**
     * Apply RTL support
     */
    applyRTLSupport() {
        document.documentElement.setAttribute('dir', 'rtl');
        document.documentElement.setAttribute('lang', 'ar');
        document.body.classList.add('rtl');
        document.body.classList.remove('ltr');
    }

    /**
     * Remove RTL support
     */
    removeRTLSupport() {
        document.documentElement.setAttribute('dir', 'ltr');
        document.documentElement.setAttribute('lang', 'en');
        document.body.classList.add('ltr');
        document.body.classList.remove('rtl');
    }

    /**
     * Get translation for a specific text
     */
    getTranslation(text) {
        return this.translations[text] || text;
    }

    /**
     * Check if text has translation
     */
    hasTranslation(text) {
        return !!this.translations[text];
    }

    /**
     * Get all available translations
     */
    getAllTranslations() {
        return this.translations;
    }
}

// Export for global access
window.ArabicLanguagePlugin = ArabicLanguagePlugin;
window.ARABIC_TRANSLATIONS = ARABIC_TRANSLATIONS; 